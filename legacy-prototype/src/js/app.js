let currentLanguage = "en";
let textScale = 1;

const languageToggle = document.querySelector("#languageToggle");
const menuButton = document.querySelector("#menuButton");
const nav = document.querySelector("#primaryNav");
const noticeSearch = document.querySelector("#noticeSearch");
const filterButtons = document.querySelectorAll("[data-filter]");
const documents = document.querySelectorAll("#documentList article");
const enquiryTabs = document.querySelectorAll("[data-enquiry-tab]");
const enquiryPanels = document.querySelectorAll("[data-enquiry-panel]");
const enquiryForms = document.querySelectorAll(".enquiry-form");
const godownDistrictFilter = document.querySelector("#godownDistrictFilter");
const godownStatusFilter = document.querySelector("#godownStatusFilter");
const godownTypeFilter = document.querySelector("#godownTypeFilter");
const godownCards = document.querySelectorAll(".godown-card");
const godownEmpty = document.querySelector("#godownEmpty");
const godownActions = document.querySelectorAll(".godown-action");

const originalText = new Map();
document.querySelectorAll("[data-i18n]").forEach((element) => {
  originalText.set(element, element.textContent);
});

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language === "hi" ? "hi" : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = language === "hi" && translations.hi[key]
      ? translations.hi[key]
      : originalText.get(element);
  });
  languageToggle.textContent = language === "hi" ? "English" : "हिन्दी";
  noticeSearch.placeholder = language === "hi" ? "सूचनाएं और निविदाएं खोजें" : "Search notices and tenders";
}

function setTextScale(nextScale) {
  textScale = Math.min(1.15, Math.max(0.92, nextScale));
  document.documentElement.style.setProperty("--font-scale", textScale.toString());
}

function filterDocuments() {
  const activeFilter = document.querySelector(".filter-button.active")?.dataset.filter || "all";
  const query = noticeSearch.value.trim().toLowerCase();

  documents.forEach((item) => {
    const matchesFilter = activeFilter === "all" || item.dataset.category === activeFilter;
    const matchesSearch = item.textContent.toLowerCase().includes(query);
    item.classList.toggle("is-hidden", !(matchesFilter && matchesSearch));
  });
}

function switchEnquiryPanel(panelName) {
  enquiryTabs.forEach((tab) => {
    const isActive = tab.dataset.enquiryTab === panelName;
    tab.classList.toggle("active", isActive);
    tab.setAttribute("aria-selected", String(isActive));
  });

  enquiryPanels.forEach((panel) => {
    panel.classList.toggle("active", panel.dataset.enquiryPanel === panelName);
  });
}

function filterGodowns() {
  const district = godownDistrictFilter?.value || "all";
  const status = godownStatusFilter?.value || "all";
  const type = godownTypeFilter?.value || "all";
  let visibleCount = 0;

  godownCards.forEach((card) => {
    const matchesDistrict = district === "all" || card.dataset.district === district;
    const matchesStatus = status === "all" || card.dataset.status === status;
    const matchesType = type === "all" || card.dataset.type === type;
    const isVisible = matchesDistrict && matchesStatus && matchesType;
    card.classList.toggle("is-hidden", !isVisible);
    if (isVisible) visibleCount += 1;
  });

  godownEmpty?.classList.toggle("is-hidden", visibleCount > 0);
}

function showPrototypeConfirmation(form) {
  const feedback = form.querySelector(".form-feedback");
  if (!feedback) return;
  feedback.textContent = currentLanguage === "hi" && translations.hi.prototypeConfirmation
    ? translations.hi.prototypeConfirmation
    : "This is a prototype. Real submission, email delivery, or database storage will be added in a later CMS/API sprint.";
}

languageToggle.addEventListener("click", () => {
  applyLanguage(currentLanguage === "en" ? "hi" : "en");
});

document.querySelector("#increaseText").addEventListener("click", () => {
  setTextScale(textScale + 0.05);
});

document.querySelector("#decreaseText").addEventListener("click", () => {
  setTextScale(textScale - 0.05);
});

menuButton.addEventListener("click", () => {
  const isOpen = nav.classList.toggle("is-open");
  menuButton.setAttribute("aria-expanded", String(isOpen));
});

nav.addEventListener("click", (event) => {
  if (event.target.tagName === "A") {
    nav.classList.remove("is-open");
    menuButton.setAttribute("aria-expanded", "false");
  }
});

filterButtons.forEach((button) => {
  button.addEventListener("click", () => {
    filterButtons.forEach((candidate) => candidate.classList.remove("active"));
    button.classList.add("active");
    filterDocuments();
  });
});

noticeSearch.addEventListener("input", filterDocuments);

enquiryTabs.forEach((tab) => {
  tab.addEventListener("click", () => {
    switchEnquiryPanel(tab.dataset.enquiryTab);
  });
});

enquiryForms.forEach((form) => {
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    showPrototypeConfirmation(form);
  });
});

[godownDistrictFilter, godownStatusFilter, godownTypeFilter].forEach((filter) => {
  filter?.addEventListener("change", filterGodowns);
});

godownActions.forEach((action) => {
  action.addEventListener("click", () => {
    switchEnquiryPanel("godown");
    const select = document.querySelector('#godown-enquiry select[name="godown"]');
    if (select && action.dataset.godownName) {
      select.value = action.dataset.godownName;
    }
  });
});
